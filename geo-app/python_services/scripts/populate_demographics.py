def main():
    sb = create_client(SUPABASE_URL, SUPABASE_SERVICE_ROLE_KEY)

    records = []
    for row in DEMOGRAPHICS:
        (
            city, state, pop, density, growth, median_age, pea, unemp,
            income, margin, edu_sup, schooling, owner, vacant,
            water, elec, drain, internet, homicide, crime, security
        ) = row

        records.append({
            "city": city,
            "state": state,
            "population": pop,
            "population_density": density,
            "population_growth_5yr_pct": growth,
            "median_age": median_age,
            "pea_pct": pea,
            "unemployment_rate": unemp,
            "avg_income_mxn": income,
            "grado_marginacion": margin,
            "pct_education_superior": edu_sup,
            "avg_schooling_years": schooling,
            "pct_owner_occupied": owner,
            "pct_vacant_housing": vacant,
            "pct_water_access": water,
            "pct_electricity": elec,
            "pct_drainage": drain,
            "pct_internet": internet,
            "homicide_rate_per_100k": homicide,
            "total_crime_rate": crime,
            "security_perception_score": round(100 - security, 1),  # Invert: higher = safer
            "data_source": "INEGI_Census_2020",
            "data_year": 2020,
        })

    logger.info(f"Uploading {len(records)} demographic records...")

    # Upsert in batches
    batch_size = 50
    for i in range(0, len(records), batch_size):
        batch = records[i : i + batch_size]
        try:
            sb.table("iainmobiliaria_demographics").upsert(
                batch, on_conflict="city,state,data_year"
            ).execute()
            logger.info(f"Uploaded {min(i + batch_size, len(records))}/{len(records)}")
        except Exception as e:
            logger.error(f"Error uploading batch starting at index {i}: {e}")
            raise

    logger.info("Demographics populated successfully")


if __name__ == "__main__":
    main()
